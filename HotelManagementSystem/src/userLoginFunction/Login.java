    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         new JToast().makeToast(this, "please wait", 1, JToast.type_of_succes);
                
        String sql ="SELECT PASSWORD,USERTYPE FROM USERDETAILS WHERE USERNAME=?";
        try {
            PreparedStatement pst = connection.prepareStatement(sql);
            pst.setString(1,jTextField1.getText());
            ResultSet rs = pst.executeQuery();
            boolean validUser = false;
            String password = null;
            String userType = null;
            while(rs.next()){
                validUser = true;
                password = rs.getString(1);
                userType = rs.getString(2);
                
                
            }
            if(validUser){
                jLabel6.setText("                 ");
            }
            else{
                jLabel6.setText("*Invalid UserName");
            }
            if(validUser){
               if(!(jPasswordField1.getText().equals(password))){
                   jLabel5.setText("*Invalid Password");
               }
               else{
                    jLabel5.setText("                 ");
                    System.out.println("came");
                    displayUi(userType);
                    getSplashView().dispose();
                    dispose();
               }
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

