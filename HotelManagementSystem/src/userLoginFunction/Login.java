    private void displayUi(String userType){
        if(userType.equals("Restaurant")){
            RestaurantManagement resMUser = new RestaurantManagement();
            resMUser.setVisible(true);
            
        }
        else if(userType.equals("Employee")){
            EmployeeManagement eUser = new EmployeeManagement();
            eUser.setVisible(true);
        }
        else if(userType.equals("Accomondation")){
            AccommondationManagement acMUser = new AccommondationManagement();
            acMUser.setVisible(true);
        }
        else if(userType.equals("supplier")){
            SupplierManagementFunction supMUser = new SupplierManagementFunction();
            supMUser.setVisible(true);
        }
        else if(userType.equals("Accountant")){
            Accountant accUser = new Accountant();
            accUser.setVisible(true);
        }
        else if(userType.equals("Kitchen")){
            Kitchen kUser = new Kitchen();
           kUser.setVisible(true);
        }
         else if(userType.equals("FrontOffice")){
            FrontOffice fUser = new FrontOffice();
           fUser.setVisible(true);
        }
         else if(userType.equals("FB")){
           FoodnBeverages fbUser = new FoodnBeverages();
           fbUser.setVisible(true);
        }  else if(userType.equals("Events")){
          EventsManagement eUser = new EventsManagement();
           eUser.setVisible(true);
        }
        else if(userType.equals("Bar")){
          BarManagement1 bUser = new BarManagement1();
           bUser.setVisible(true);
        }
         else if(userType.equals("Housekeeping")){
          Housekeeping hUser = new Housekeeping();
           hUser.setVisible(true);
        }
         else if(userType.equals("Admin")){
          loginnav aUser = new loginnav();
           aUser.setVisible(true);
        }
         else {
          JOptionPane.showMessageDialog(null, 
                              "Invalid User", 
                              "ALERT MESSAGE", 
                              JOptionPane.WARNING_MESSAGE);
         }
        
        
        
       
    }

